/**
 * Color type definitions
 */

/** Represents an RGB color as a tuple of three numbers (0-255). */
export type RGBColor = [number, number, number];

/** Represents an RGBA color as a tuple of four numbers (0-255 for RGB, 0-1 for Alpha). */
export type RGBAColor = [number, number, number, number];

/** Represents an HSL color as a tuple of three numbers (0-360 for Hue, 0-100 for Saturation/Lightness). */
export type HSLColor = [number, number, number]; 
